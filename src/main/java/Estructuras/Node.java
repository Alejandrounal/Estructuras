package Estructuras;

/**
 *
 * @author Alejandro
 */
public class Node {
    private Node next;
    private int data;
    private Node prev;

    public Node() {
        this(0);
    }

    public Node(int data) {
        this.next = null;
        this.data = data;
       
    }

    public Node getNext() {
        return next;
    }

    public void setNext(Node next) {
        this.next = next;
    }

    public int getData() {
        return data;
    }

    public void setData(int data) {
        this.data = data;
    }

    public Node getPrev() {
        return prev;
    }

    public void setPrev(Node prev) {
        this.prev = prev;
    }
   
}
